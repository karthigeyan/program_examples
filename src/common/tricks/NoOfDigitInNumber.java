package common.tricks;

public class NoOfDigitInNumber {
    public static void main(String args[]){

        int data = 2345442;
        System.out.println(Math.log10(22));
        System.out.println(Math.floor(Math.log10(data)));
        System.out.println(Math.floor(Math.log10(data)) + 1);
    }
}
